# This line prints the last element of the list by calculating the index manually
# len(lst) gives the total number of elements, and we subtract 1 to get the last index
print(list[len(list) - 1])

# This line also prints the last element, but in a shorter and more common way using -1 indexing
# In Python, lst[-1] always gives the last item in the list
print(list[-1])



